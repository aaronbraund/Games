number = -2
if number >= 10:
    print("This number is over 10")
elif number > 5:
    print("This number is greater than 5 but less than 10")
elif number > 3:
    print("This number is greater than 3 but less than 5 or 10")
elif number > 1:
    print("This number is over 1 but less than 3")
else:
    print("That's a zero or negative number -_-")